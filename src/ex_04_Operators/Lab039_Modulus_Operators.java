package ex_04_Operators;

public class Lab039_Modulus_Operators {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        System.out.println(a%b);
        // Mod -> Remainder,
//        10 | 20 |  2 - Quoinet
//           | 20 |
//           -------
//             0 - Remainder
//           ----
        System.out.println(13%7);
        //        7 | 13 |  1 - Quoinet
        //           | 7 |
        //           -------
        //             6 - Remainder
        //           ----
//      Output = 0, 6
    }
}
