package ex_09_switch;

public class Lab_096_Interview {
    public static void main(String[] args) {
        char code = 'C';

        switch (code){
            default:
                System.out.println("Helllooooo");
                break;
            case 'A':
                System.out.println("65");
            case 'B':
                System.out.println("66");
        }
    }
}
